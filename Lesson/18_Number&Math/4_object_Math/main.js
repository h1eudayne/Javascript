// Math.abs()
console.log(Math.abs(-5)); // 5
console.log(Math.abs(5)); // 5

//------------------------------------------------------------------------------

// Math.ceil()
// Làm trong số lên đến số nguyên gần nhất
console.log(Math.ceil(5.1)); // 6
console.log(Math.ceil(5.9)); // 6

//------------------------------------------------------------------------------

// Math.floor()
// Làm tròn số xuống đến số nguyên gần nhất
console.log(Math.floor(5.1)); // 5
console.log(Math.floor(5.9)); // 5

//------------------------------------------------------------------------------

// Math.round()
// Làm tròn số đến số nguyên gần nhất
// Dưới 0.5 sẽ làm tròn xuống, từ 0.5 trở lên sẽ làm tròn lên
console.log("Math.round()");
console.log(Math.round(5.1)); // 5
console.log(Math.round(5.5)); // 6

//------------------------------------------------------------------------------

// Math.max()
// Tìm giá trị lớn nhất trong một danh sách các số
console.log(Math.max(1, 2, 3)); // 3
console.log(Math.max(5, 10, 2)); // 10
console.log(Math.max()); // -Infinity


//------------------------------------------------------------------------------

// Math.min()
// Tìm giá trị nhỏ nhất trong một danh sách các số
console.log(Math.min(1, 2, 3)); // 1
console.log(Math.min(5, 10, 2)); // 2
console.log(Math.min()); // Infinity

//------------------------------------------------------------------------------

// Math.pow()
// Tính lũy thừa của một số
console.log(Math.pow(2, 3)); // 8 (2^3)

//------------------------------------------------------------------------------

// Math.sqrt()
// Tính căn bậc hai của một số
console.log(Math.sqrt(16)); // 4

//------------------------------------------------------------------------------

// Math.random()
// Tạo một số ngẫu nhiên từ 0 đến 1
console.log(Math.random());

//------------------------------------------------------------------------------

// Math.trunc()
// Trả về phần nguyên của một số, loại bỏ phần thập phân
console.log(Math.trunc(5.9)); // 5

//------------------------------------------------------------------------------

// Math.PI
// Hằng số Pi
console.log(Math.PI); // 3.141592653589793

