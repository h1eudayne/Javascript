function calculateRectangleArea(a, b) {
  return a * b;
}

function calculateTriangleArea(a, b) {
  return (a * b) / 2;
}

function calculateSquareArea(a) {
  return a * a;
}

// Sample usage
console.log(calculateRectangleArea(2, 5)); // 10
console.log(calculateTriangleArea(10, 4)); // 20
console.log(calculateSquareArea(5)); // 25
